
public class Calculadora {
	
	public double soma(double x, double y){
		
		return x + y;
		
	}
	
	public double subtração(double x, double y) {
		
		return x - y;
	}
	
	public double divisão(double x, double y) {
		
		return x/y;
	}
	
	public double multiplicação(double x, double y) {
		
		return x*y;
	}
	
	
}
